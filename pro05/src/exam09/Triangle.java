package exam09;

public class Triangle {
	public double area(double width, double height) {
		return (width * height) / 2;
	}
		
	public double round(double width, double height) {
		return (width * height) / 2;
	}
	
	public double round(double length) {
		double result;
		result = length * 3;
		return result;
	}
}
